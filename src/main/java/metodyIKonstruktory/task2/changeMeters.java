package metodyIKonstruktory.task2;

public class changeMeters {



    double metersPerCentimeters(double numbers){
        return numbers * 100;
    }

    double metersPerMillimeters(double numbers){
        return numbers * 100 * 10;
    }

    double centimetersPerMeters(double numbers){
        return numbers / 100;
    }

    double MillimetersPerMeters(double numbers){
        return numbers / 10 / 100;
    }

}
